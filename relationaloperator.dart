void main() {
  int a = 2;
  int b = 3;

  //graeter between a and b
  var c = a > b;
  print("a is greater then $c");

  //smaller between a and b
  var d = a < b;
  print("a is smaller then $d");

  //greater than or equal to between a and b
  var e = a >= b;
  print("a is greater then or equal to $e");

  //less tha or equal to between a and b
  var f = a <= b;
  print("a is less then or equal to $f");

  //equality between a and b
  var g = b == a;
  print("a is equal to $g");

  //unequality between a and b
  var h = b!= a;
  print("a is not equal to $h");
}
