void main() {
  int a = 5;
  int b = 7;

  //performing bitwise AND on a and b
  var c = a & b;
  print(c);

  //bitwise or
  var d = a | b;
  print(d);

  //XOR
  var e = a ^ b;
  print(e);

  //bitwise not
  var f = ~a;
  print(f);

  //left shift
  var g = a << b;
  print(g);

  //right - shift
  var h = a >> b;
  print(h);
}
