void main() {
  int a = 2;
  int b = 3;

  //adding a and b
  var c = a + b;
  print("sum of a and b is $c");

  //substracting a and b
  var d = a - b;
  print("The difference between a and b is $d");

  //using unary minus
  var e = -d;
  print("The negation of difference between a and b is $e");

  //multification of a and b
  var f = a * b;
  print("The product of a and b is $f");

  //division of a and b
  var g = b / a;
  print("The quotient of a and b is $g");

  // using ~/ to divide a and b
  var h = b ~/ a;
  print("The quotient of a and b is $h");

  //reminder of a and b
  var i = b % a;
  print("The reminder of a and b is $i");
}
